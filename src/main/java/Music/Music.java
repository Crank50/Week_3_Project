public class Music {

    private String name;
    private String favoriteBand;
    private String favoriteVenue;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFavoriteBand() {
        return favoriteBand;
    }

    public void setFavoriteBand(String favoriteBand) {
        this.favoriteBand = favoriteBand;
    }

    public String getFavoriteVenue() {
        return favoriteVenue;
    }

    public void setFavoriteVenue(String favoriteVenue) {
        this.favoriteVenue = favoriteVenue;
    }
}
